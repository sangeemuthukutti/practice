package com.parking;

public class Car {
public String brand;
public int carno;
public String colour;
public String getBrand() {
	return brand;
}
public void setBrand(String brand) {
	this.brand = brand;
}
public int getCarno() {
	return carno;
}
public void setCarno(int carno) {
	this.carno = carno;
}
public String getColour() {
	return colour;
}
public void setColour(String colour) {
	this.colour = colour;
}

}
